package Ejercicio85;

public class Bloque {
    
    private double largo;
    private double ancho;
    private double altura;

    public Bloque(double largo, double ancho, double altura) {
        this.largo = largo;
        this.ancho = ancho;
        this.altura = altura;
    }

    public double getLargo() {
        return largo;
    }

    public double getAncho() {
        return ancho;
    }

    public double getAltura() {
        return altura;
    }

}
